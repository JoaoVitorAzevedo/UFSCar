● Fazer um programa com um conjunto de regras para
implementação do consultor financeiro
– conta de poupança inadequada -> poupança.
– conta de poupança adequada e uma renda adequada ->
ações.
– renda pequena e conta de poupança adequada -> poupança
e ações.
● Para uma poupança adequada, o indivíduo deve ter no mínimo
R$5.000,00 no banco para cada dependente. Uma renda
adequada deve ser estável e fornecer ao menos R$15.000,00
ao ano, mais um adicional de R$4.000,00 por cada dependente.